//Atualização de Documentos
//update e replace

//updateOne(filter, update, options) - atualiza um único documento que corresponde ao filtro.
//updateMany(filter, update, options) - atualiza múltiplos documentos que corresponde ao filtro.
//replaceOne(filter, replacement, options) - substitui um único documento inteiro pelo novo documento fornecido.

//Operadores de Atualização

//Modificação de campos
//set, unset e rename

//set - define ou altera um campo específico
//unset - remove um campo
//rename - renomeia um campo

//Operações Matemáticas
//inc, mul, min, max

//inc - incrementa o valor de um campo numérico
//mul - multiplica um valor
//min - reduz um valor, desde que o valor informado seja menor que o valor que vai ser reduzido
//max - aumenta um valor, desde que o valor informado seja maior que o valor que vai ser aumentado

//Operações de Array
//push, pop, pull, addToSet, pullAll e each

//push - adiciona um elemento a um array
//pop - remove o primeiro ou último elemento
//pull - remove elementos específicos
//addToSet - adiciona um item se ele não existir
//pullAll - remove múltiplos valores específicos de um array
//each - adiciona múltiplos elementos