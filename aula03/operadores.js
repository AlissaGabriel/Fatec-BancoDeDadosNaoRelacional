//Operadores de Comparação
//-eq, ne, gt, lt, gte, lte

//eq = este operador retorna documentos cujo valor de um campo específico seja igual ao valor informado
//ne = retorna documentos cujo valor de um campo seja diferente do valor especificado.
//gt = retorna documentos cujo valor de um campo seja maior que o especificado
//lt = retorna documentos cujo valor de um campo seja menor que o especificado
//gte = maior ou igual
//lte = menor ou igual

//Operadores Lógicos
//-and, or, nor e not

//and = exige que todas as condições especificadas sejam verdadeiras
//or = retorna documentos que satisfaçam pelo menos uma das condições especificadas
//nor = oposto de $or, excluindo documentos que satisfaçam qualquer uma das condições listadas
//not = nega uma condição específica

//Operadores de Elemento
//-type e exists

//type = filtra documentos com base no tipo de dado armazenado em um campo
//exists = verifica se um campo está presente ou não em um documento

